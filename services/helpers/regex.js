const regex = {
    password    : new RegExp("^([a-z0-9]{5,})$"),
    email       : /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
    phone       : /^[+]*[(]{0,1}[0-9]{1,4}[)]{0,1}[-\s\./0-9]*$/,
    name        : /^[a-z ,.'-]+$/i,
    fullName    : /^[a-z ,.'-]+$/i,
}
export default regex